package com.cryingonion.instashop.instagram.holder;

public class IgFollowsHolder {

	private String mFullName;
	private String mProfilePictureUrl;
	private String mWebsiteUrl;
	private String mBio;
	private String mUserId;
	private String mUsername;
	
	private IgUserInfoHolder mUserInfo;


	public String getmFullName() {
		return mFullName;
	}

	public void setmFullName(String mFullName) {
		this.mFullName = mFullName;
	}

	public String getmProfilePictureUrl() {
		return mProfilePictureUrl;
	}

	public void setmProfilePictureUrl(String mProfilePictureUrl) {
		this.mProfilePictureUrl = mProfilePictureUrl;
	}

	public String getmWebsiteUrl() {
		return mWebsiteUrl;
	}

	public void setmWebsiteUrl(String mWebsiteUrl) {
		this.mWebsiteUrl = mWebsiteUrl;
	}

	public String getmBio() {
		return mBio;
	}

	public void setmBio(String mBio) {
		this.mBio = mBio;
	}
	
	public String getmUserId() {
		return mUserId;
	}

	public void setmUserId(String mUserId) {
		this.mUserId = mUserId;
	}
	
	public String getmUsername() {
		return mUsername;
	}

	public void setmUsername(String mUsername) {
		this.mUsername = mUsername;
	}
	
	public IgUserInfoHolder getmUserInfo() {
		return mUserInfo;
	}

	public void setmUserInfo(IgUserInfoHolder mUserInfo) {
		this.mUserInfo = mUserInfo;
	}

}
